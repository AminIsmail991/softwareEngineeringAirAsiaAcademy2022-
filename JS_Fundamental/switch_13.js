
const color = 'red'  //it is case sensitive 

switch(color){
    case 'red':
        console.log('color is red');
        break;
    case 'green':
        console.log('color is green');
        break;
    case 'pink':
        console.log('color is pink');
        break;
        default:
            console.log('no valid color');
}